/*Atividade 1 - Soma dos ímpares de 1 a 30
Faça um programa que use for para somar todos os números ímpares de 1 até 30 e
mostrar o resultado final no console */

let soma = 0;
for(let i = 1; i <= 30; i++){
    if( i % 2 !== 0 ){
      soma += i; 
      console.log(`A soma é: ${soma}`);
    }
}


















